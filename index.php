    <script>
        new WOW().init();


        $(document).ready(function() {
            $('.hamburger').click(function() {
                if (!$('#navContent').hasClass('collapsing')) {
                    $(this).toggleClass('is-active');
                }
            });

            $(document).click(function(e) {
                let el = $(e.target);

                if (el.parents('li.has-child')) {
                    $('#menu ul li.has-child').removeClass('open');
                    el.parents('li.has-child').addClass('open');
                } else {
                    $('#menu ul li.has-child').removeClass('open');
                }

                if (el.is('a') && el.parent().hasClass('has-child')) {
                    if (!el.hasClass('open')) {
                        e.preventDefault();
                        el.addClass('open')
                    }
                } else {
                    $('#menu a.open').removeClass('open');
                }
            })
        });
    </script>

